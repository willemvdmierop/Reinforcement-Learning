    step_until_goal = 0
    total_reward = 0
    reward_until_goal = 0
    done = False
    steps_traffic = 0
    steps_no_traffic = 0 
    while not done:
      ###### traffic on or off ########
      if random.uniform(0,1) > threshold_traffic: #make traffic true or false
        env.trafic_ON = True
        steps_traffic += 1
      else:
        env.trafic_ON = False
        steps_no_traffic += 1
      
      x,y = env.decode_state(env.state)[0], env.decode_state(env.state)[1]
