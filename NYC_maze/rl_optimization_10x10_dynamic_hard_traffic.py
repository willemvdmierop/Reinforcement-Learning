while gamma <= 1:
  ##### epsilon values ####
  epsilon = 0.7  ############
  min_epsilon = 0.05 #######
  epsilon_decay = 1e-5 ####
  #########################
  # random amount of times during training where traffic is true
  threshold_traffic = 0.8
  ########################
  epochs = []
  average_rewards = []
  steps = []
  steps_until_goal = []
  reward_until_goal_list = []
